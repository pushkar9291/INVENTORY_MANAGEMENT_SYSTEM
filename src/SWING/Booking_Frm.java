    private void book_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_book_buttonActionPerformed
        // TODO add your handling code here:
        
          if(cid_txt.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this,"CUSTOMER ID IS EMPTY");
        }
        else if(cname_txt.getText().isEmpty())
        {
             JOptionPane.showMessageDialog(this,"CUSTOMER NAME IS EMPTY");
        }
        else if(cphno_txt.getText().isEmpty())
        {
             JOptionPane.showMessageDialog(this,"CUSTOMER PHONE NUMBER IS EMPTY");
        }
        else if(pid_combo.getSelectedItem().toString().isEmpty())
        {
             JOptionPane.showMessageDialog(this,"PRODUCT ID IS EMPTY");
        }
        else if(pqty_txt.getText().isEmpty())
        {
             JOptionPane.showMessageDialog(this,"PRODUCT QTY IS EMPTY");
        }
        else if(shid_combo.getSelectedItem().toString().isEmpty())
        {
             JOptionPane.showMessageDialog(this,"SHOWROOM ID IS EMPTY");
        }
        else if(date_txt.getText().isEmpty())
        {
             JOptionPane.showMessageDialog(this,"PURCHASE DATE IS EMPTY");
        }
        else
        {
        this.insertData();
        this.refreshData();
        JOptionPane.showMessageDialog(this,"ORDER BOOKED SUCCESSFULLY");
        this.getData();
        }
        
        
    }//GEN-LAST:event_book_buttonActionPerformed

