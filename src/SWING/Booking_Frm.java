public void insertData()
  {
      try
      {
          ConnectionFactory con=new ConnectionFactory();
          cn=con.getConn();
          ps=cn.prepareStatement(insert_sql);
          ps.setString(1, cid_txt.getText());
          ps.setString(2, cname_txt.getText());
          ps.setString(3, cphno_txt.getText());
          ps.setString(4, pid_combo.getSelectedItem().toString());
          ps.setString(5, pqty_txt.getText());
          ps.setString(6, shid_combo.getSelectedItem().toString());
          ps.setString(7,date_txt.getText());
          ps.executeUpdate();
          cn.commit();
      }
      catch(SQLException se)
      {
          se.printStackTrace();
      }
      
  }
public void getShidData()
  {
      try
      {
          ConnectionFactory con=new ConnectionFactory();
          cn=con.getConn();
          st=cn.createStatement();
          rs=st.executeQuery(select_shid_sql);
          while(rs.next())
          {
              shid_combo.addItem(rs.getString(1));
          }
        }
      catch(SQLException se)
      {
          se.printStackTrace();
      }
      
  }
public void getPidData()
  {
      try
      {
          ConnectionFactory con=new ConnectionFactory();
          cn=con.getConn();
          st=cn.createStatement();
          rs=st.executeQuery(select_pid_sql);
          while(rs.next())
          {
              pid_combo.addItem(rs.getString(1));
          }
          
      }
      catch(SQLException se)
      {
          se.printStackTrace();
      }
      
  }
public void getData()
  {
      try
      {
          ConnectionFactory con=new ConnectionFactory();
          cn=con.getConn();
          st=cn.createStatement();
          rs=st.executeQuery(select_sql);
          booking_table.setModel(DbUtils.resultSetToTableModel(rs));
      }
      catch(SQLException se)
      {
          se.printStackTrace();
      }
   }

