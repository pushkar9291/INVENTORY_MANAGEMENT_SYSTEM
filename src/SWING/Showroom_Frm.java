           if(shid_txt.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this,"SHOWROOM ID IS EMPTY");
        }
        else if(shnm_txt.getText().isEmpty())
        {
             JOptionPane.showMessageDialog(this,"SHOWROOM NAME IS EMPTY");
        }
