  public void insertData()
  {
      try
      {
          ConnectionFactory con=new ConnectionFactory();
          cn=con.getConn();
          ps=cn.prepareStatement(insert_sql);
          ps.setString(1,suppid_txt.getText());
          ps.setString(2,suppnm_txt.getText());
          ps.setString(3, suppphno_txt.getText());
          ps.setString(4,suppemail_txt.getText());
          ps.executeUpdate();
          cn.commit();
      }
      catch(SQLException se)
      {
          se.printStackTrace();
      }
      
  }
 public void getData()
  {
      try
      {
          ConnectionFactory con=new ConnectionFactory();
          cn=con.getConn();
          st=cn.createStatement();
          rs=st.executeQuery(select_sql);
          supplier_table.setModel(DbUtils.resultSetToTableModel(rs));
          
      }
      catch(SQLException se)
      {
          se.printStackTrace();
      }
      
  }
 public void deleteData()
  {
      try
      {
          ConnectionFactory con=new ConnectionFactory();
          cn=con.getConn();
          ps=cn.prepareStatement(delete_sql);
          ps.setString(1,suppid_txt.getText());
          ps.executeUpdate();
          cn.commit();
      }
      catch(SQLException se)
      {
          se.printStackTrace();
      }
      
  }
 public void updateData()
  {
      try
      {
          ConnectionFactory con=new ConnectionFactory();
          cn=con.getConn();
          ps=cn.prepareStatement(update_sql);
           ps.setString(1,suppnm_txt.getText());
          ps.setString(2,suppphno_txt.getText());
          ps.setString(3,suppemail_txt.getText());
          ps.setString(4,suppid_txt.getText());
          
          ps.executeUpdate();
          cn.commit();
      }
      catch(SQLException se)
      {
          se.printStackTrace();
      }
      
  }
 public void refreshData()
  {
      suppid_txt.setText("");
      suppnm_txt.setText("");
      suppphno_txt.setText("");
      suppemail_txt.setText("");
  }
