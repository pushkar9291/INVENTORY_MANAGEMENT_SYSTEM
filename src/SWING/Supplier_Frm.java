    private void save_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_buttonActionPerformed
        // TODO add your handling code here:
          if(suppid_txt.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this,"SUPPLIER ID IS EMPTY");
        }
        else if(suppnm_txt.getText().isEmpty())
        {
             JOptionPane.showMessageDialog(this,"SUPPLIER NAME IS EMPTY");
        }
        else if(suppphno_txt.getText().isEmpty())
        {
             JOptionPane.showMessageDialog(this,"SUPPLIER PHONE NO. IS EMPTY");
        }
        
        else if(suppemail_txt.getText().isEmpty())
        {
             JOptionPane.showMessageDialog(this,"SUPPLIER EMAIL IS EMPTY");
        }
        else
        {
        this.insertData();
        this.refreshData();
        JOptionPane.showMessageDialog(this,"DATA INSERTED SUCCESSFULLY");
        this.getData();
        }
    }//GEN-LAST:event_save_buttonActionPerformed

